    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        try {

            String mobile = jTextField1.getText();
            String email = jTextField2.getText();
            String fname = jTextField3.getText();
            String lname = jTextField4.getText();
            String nic = jTextField5.getText();
            String password = String.valueOf(jPasswordField1.getPassword());
            String gender = String.valueOf(jComboBox2.getSelectedItem());
            String type = String.valueOf(jComboBox1.getSelectedItem());

            if (mobile.isEmpty()) {
                JOptionPane.showMessageDialog(this, "Please enter employee mobile", "Warning", JOptionPane.WARNING_MESSAGE);
            } else if (!mobile.matches("^07[01245678]{1}[0-9]{7}$")) {
                JOptionPane.showMessageDialog(this, "Invalid employee mobile", "Warning", JOptionPane.WARNING_MESSAGE);
            } else if (email.isEmpty()) {
                JOptionPane.showMessageDialog(this, "Please enter employee email", "Warning", JOptionPane.WARNING_MESSAGE);
            } else if (!email.matches("^(?=.{1,64}@)[A-Za-z0-9\\+_-]+(\\.[A-Za-z0-9\\+_-]+)*@"
                    + "[^-][A-Za-z0-9\\+-]+(\\.[A-Za-z0-9\\+-]+)*(\\.[A-Za-z]{2,})$")) {
                JOptionPane.showMessageDialog(this, "Invalid employee email", "Warning", JOptionPane.WARNING_MESSAGE);
            } else if (fname.isEmpty()) {
                JOptionPane.showMessageDialog(this, "Please enter employee first name", "Warning", JOptionPane.WARNING_MESSAGE);
            } else if (lname.isEmpty()) {
                JOptionPane.showMessageDialog(this, "Please enter employee last name", "Warning", JOptionPane.WARNING_MESSAGE);
            } else if (nic.isEmpty()) {
                JOptionPane.showMessageDialog(this, "Please enter employee nic", "Warning", JOptionPane.WARNING_MESSAGE);
            } else if (password.isEmpty()) {
                JOptionPane.showMessageDialog(this, "Please enter employee password", "Warning", JOptionPane.WARNING_MESSAGE);
            } else if (!password.matches("^(?=.*[a-z])(?=.*[A-Z])(?=.*\\d)[a-zA-Z\\d]{8,}$")) {
                JOptionPane.showMessageDialog(this, "Invalid Password :Minimum eight characters, at least one uppercase letter, one lowercase letter and one number:", "Warning", JOptionPane.WARNING_MESSAGE);
            } else if (gender.equals("Select")) {
                JOptionPane.showMessageDialog(this, "Please select gender", "Warning", JOptionPane.WARNING_MESSAGE);
            } else if (type.equals("Select")) {
                JOptionPane.showMessageDialog(this, "Please select employee type", "Warning", JOptionPane.WARNING_MESSAGE);
            } else {

                ResultSet resultSet = MySQL.execute("SELECT * FROM `employee` WHERE `email`='" + email + "' OR `nic`='" + nic + "' OR `mobile`='" + mobile + "'");

                if (resultSet.next()) {
                    JOptionPane.showMessageDialog(this, "Email, Mobile or NIC already used", "Warning", JOptionPane.WARNING_MESSAGE);
                } else {

                    Date date = new Date();
                    SimpleDateFormat sdf = new SimpleDateFormat("yyyy-MM-dd");

                    MySQL.execute("INSERT INTO `employee`(`mobile`,`email`,`first_name`,`last_name`,`nic`,`employee_type_id`,`date_registered`,`password`,`gender_id`) "
                            + "VALUES('" + mobile + "','" + email + "','" + fname + "','" + lname + "','" + nic + "',"
                            + "'" + employeeTypeMap.get(type) + "','" + sdf.format(date) + "','" + password + "','" + employeeGenderMap.get(gender) + "')");

                    loadEmployee();
                    reset();

                }
            }

        } catch (Exception e) {
            e.printStackTrace();
        }
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        // TODO add your handling code here:
        int row = jTable1.getSelectedRow();
        if (evt.getClickCount() == 1) {
            
            jButton1.setEnabled(false);
            jButton2.setEnabled(true);

            String mobile = String.valueOf(jTable1.getValueAt(row, 0));
            jTextField1.setText(mobile);
            jTextField1.setEditable(false);
            
            String email = String.valueOf(jTable1.getValueAt(row, 1));
            jTextField2.setText(email);

            String fname = String.valueOf(jTable1.getValueAt(row, 2));
            jTextField3.setText(fname);

            String lname = String.valueOf(jTable1.getValueAt(row, 3));
            jTextField4.setText(lname);

            String nic = String.valueOf(jTable1.getValueAt(row, 4));
            jTextField5.setText(nic);

            String gender = String.valueOf(jTable1.getValueAt(row, 7));
            jComboBox2.setSelectedItem(gender);

            String password = String.valueOf(jTable1.getValueAt(row, 6));
            jPasswordField1.setText(password);

            String type = String.valueOf(jTable1.getValueAt(row, 5));
            jComboBox1.setSelectedItem(type);

        } else if (evt.getClickCount() == 3) {
            String mobile = String.valueOf(jTable1.getValueAt(row, 0));
            
            Address_View addresView = new Address_View(this,true,mobile);
            addresView.setVisible(true);
        }
        
        if(evt.getClickCount()==2){
            if(salary!=null){
                salary.getjTextField1().setText(String.valueOf(jTable1.getValueAt(row, 0)));
                salary.getjTextField1().grabFocus();
                
                if(String.valueOf(jTable1.getValueAt(row, 5)).equals("Super Admin")){
                    salary.setUserType(1);
                }else if(String.valueOf(jTable1.getValueAt(row, 5)).equals("Admin")){
                    salary.setUserType(2);
                }else if(String.valueOf(jTable1.getValueAt(row, 5)).equals("Chashier")){
                    salary.setUserType(3);
                }
                
                this.dispose();
            }
        }
    }//GEN-LAST:event_jTable1MouseClicked


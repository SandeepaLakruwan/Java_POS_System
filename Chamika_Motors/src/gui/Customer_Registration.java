    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        // TODO add your handling code here:
        int row = jTable1.getSelectedRow();

        String mobile = String.valueOf(jTable1.getValueAt(row, 0));
        String name = String.valueOf(jTable1.getValueAt(row, 1));

        jTextField1.setText(mobile);
        jTextField2.setText(name);
        
        jTextField3.setEditable(false);
        jTextField1.setEditable(false);
        jButton1.setEnabled(false);
        jButton2.setEnabled(true);
        
        if(evt.getClickCount()==2){
            if(invoice!=null){
                invoice.getjTextField2().setText(String.valueOf(jTable1.getValueAt(row, 0)));
                invoice.getjLabel14().setText(String.valueOf(jTable1.getValueAt(row, 2)));
                invoice.getjLabel16().setText(String.valueOf(jTable1.getValueAt(row, 1)));
                
                this.dispose();
            }
        }
        
        try {
            
            ResultSet resultSet = MySQL.execute("SELECT * FROM `invoice` INNER JOIN `invoice_item` "
                    + "ON `invoice`.`id`=`invoice_item`.`invoice_id` INNER JOIN `stock` ON "
                    + "`invoice_item`.`stock_id`=`stock`.`id` WHERE "
                    + "`invoice`.`customer_mobile`='"+String.valueOf(jTable1.getValueAt(row, 0))+"'");
            
            double total = 0;
            double totalPaid = 0;
            
            HashMap<Double, Double> invoices = new HashMap<>();
            
            while(resultSet.next()){
                double qty = resultSet.getDouble("invoice_item.qty");
                double selling_price = resultSet.getDouble("stock.selling_price");
                
                double itemTotal = qty*selling_price;
                
                total += itemTotal;

                invoices.put(resultSet.getDouble("invoice.id"), resultSet.getDouble("invoice.paid_amount"));
                
            }
            
            for (Double paid : invoices.values()) {
                totalPaid+=paid;
            }
            
            jLabel5.setText(String.valueOf(invoices.size()));
            jLabel7.setText(String.valueOf(total-totalPaid));
        } catch (Exception e) {
            e.printStackTrace();
        }
    }//GEN-LAST:event_jTable1MouseClicked


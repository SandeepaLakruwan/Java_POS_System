    private void loadSuppliers(String column, String type, String search) {

        try {
            ResultSet resultSet = MySQL.execute("SELECT * FROM `supplier` INNER JOIN `company` ON "
                    + "`supplier`.`company_id`=`company`.`id` WHERE `mobile` LIKE '" + search + "%' ORDER BY `" + column + "` " + type + "");

            DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
            model.setRowCount(0);

            while (resultSet.next()) {
                Vector<String> vector = new Vector<>();
                vector.add(resultSet.getString("mobile"));
                vector.add(resultSet.getString("supplier.name"));
                vector.add(resultSet.getString("company.name"));
                model.addRow(vector);
            }
        } catch (Exception e) {
            e.printStackTrace();
        }
    }


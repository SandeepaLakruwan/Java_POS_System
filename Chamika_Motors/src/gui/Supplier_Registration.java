    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        // TODO add your handling code here:
        int row = jTable1.getSelectedRow();

        jTextField1.setText(String.valueOf(jTable1.getValueAt(row, 0)));
        jTextField2.setText(String.valueOf(jTable1.getValueAt(row, 1)));
        jLabel8.setText(String.valueOf(jTable1.getValueAt(row, 2)));

        jTextField1.setEditable(false);
        jButton3.setEnabled(false);
        jButton4.setEnabled(true);

        if(evt.getClickCount()==2){
            if(grn!=null){
                grn.getjTextField2().setText(String.valueOf(jTable1.getValueAt(row, 0)));
                grn.getjLabel22().setText(String.valueOf(jTable1.getValueAt(row, 1)));
                this.dispose();
            }
        }
        try {

            ResultSet resultSet = MySQL.execute("SELECT * FROM `grn` INNER JOIN `grn_item` "
                    + "ON `grn`.`id`=`grn_item`.`grn_id` WHERE "
                    + "`grn`.`supplier_mobile`='" + String.valueOf(jTable1.getValueAt(row, 0)) + "'");

            double total = 0;
            double totalPaid = 0;

            HashMap<Double, Double> grns = new HashMap<>();

            while (resultSet.next()) {
                double qty = resultSet.getDouble("grn_item.qty");
                double buying_price = resultSet.getDouble("grn_item.buying_price");

                double itemTotal = qty * buying_price;

                total += itemTotal;

                grns.put(resultSet.getDouble("grn.id"), resultSet.getDouble("grn.paid_amount"));

            }

            for (Double paid : grns.values()) {
                totalPaid += paid;
            }

            jLabel7.setText(String.valueOf(grns.size()));
            jLabel5.setText(String.valueOf(total - totalPaid));
        } catch (Exception e) {
            e.printStackTrace();
        }
    }//GEN-LAST:event_jTable1MouseClicked

